package BitManipulation;

public class Xor {
    public static void main(String[] args) {
        int p=0,q=3,r=5;
        r=12+r;
        if((q+r)<(r-p)||6<r){
            if ((1&q)<(r^1)){
                p=r+q;
            }
            q=(6+9)&q;
            r=q+r;
        }
        r=q+q;
        System.out.println((p+q+r));
    }
}
